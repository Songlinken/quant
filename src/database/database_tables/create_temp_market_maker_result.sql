CREATE TABLE TEMP_MARKET_MAKER_RESULT (
    ACCOUNT_ID                  INT         NOT NULL,
    DATA_FROM_DATE              DATE        NOT NULL,
    SYMBOL                      CHAR(6)     NOT NULL,
    PLACE_CANCEL_TIME_DIFF      NUMERIC,
    BID_ASK_RATIO_MEDIAN        NUMERIC,
    BID_ASK_RATIO_STD           NUMERIC,
    IS_INSTITUTIONAL            BOOLEAN,
    FILL_BUY_ORDERS_COUNT       NUMERIC,
    FILL_SELL_ORDERS_COUNT      NUMERIC,
    FILL_BUY_ORDERS_QUANTITY    NUMERIC,
    FILL_SELL_ORDERS_QUANTITY   NUMERIC,
    TOTAL_ORDERS                INT,
    CANCEL_ORDERS               NUMERIC,
    MAKER_COUNTS                NUMERIC,
    TAKER_COUNTS                NUMERIC,
    PRIMARY KEY (ACCOUNT_ID, DATA_FROM_DATE, SYMBOL)
);

CREATE INDEX idx_temp_market_maker_result_1 ON TEMP_MARKET_MAKER_RESULT (SYMBOL);
CREATE INDEX idx_temp_market_maker_result_2 ON TEMP_MARKET_MAKER_RESULT (DATA_FROM_DATE);